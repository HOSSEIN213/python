# لیست میوه‌های موجود (لیست)
میوه_ها = ["سیب", "موز", "پرتقال"]

# تاپل تعداد هر میوه (به ترتیب با لیست)
تعداد = (3, 2, 5)

# مجموعه میوه‌های تکراری (برای حذف تکرار)
مجموعه_میوه = {"سیب", "موز", "پرتقال", "سیب"}  # خودکار "سیب" تکراری رو حذف می‌کنه

# کارهایی که باید بکنی:
print("لیست میوه‌ها:", میوه_ها)
print("دومین میوه:", میوه_ها[1])
print("تعداد پرتقال:", تعداد[2])
print("مجموعه میوه‌ها (تکراری نداره):", مجموعه_میوه)

# اضافه کردن یه میوه جدید به لیست
میوه_ها.append("انگور")
print("لیست بعد از اضافه:", میوه_ها)

# سعی کن تعداد موز رو عوض کنی (تاپل غیرقابل تغییره)
# تعداد[1] = 4  # این خطا میده، فقط برات توضیح بده
print("نکته: تاپل قابل تغییر نیست، پس تعداد رو نمیشه عوض کرد.")

